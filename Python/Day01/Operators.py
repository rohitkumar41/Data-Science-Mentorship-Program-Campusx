# Arithmetic
# Relational
# Logical
# Bitwise
# Assignment
# Membership

# Arithmetic Operators
print(5+6)
print(5-6)
print(5*6)
print(5/10)
print(5%10)
print(5//2)
print(5**2)

# Relational Operator
print(4>5)
print(4<5)
print(4>=4)
print(5<=5)
print(5==4)
print(5!=4)

# Logical Operator
print(1 and 0)
print(1 or 0)
print(not 1)

# Bitwise Operator

# bitwise and
print(2 & 3)

# bitwise or
print(2 | 3)

# bitwise xor
print(2 ^ 3)

print(~3)

print(4 >> 2)
print(5 << 2)


# Assignment Operator
# =
# a = 2

a = 2

# a = a % 2
a %= 2

# a++ ++a
print(a)

# MemberShip Operator

# in/not in
print('D' not in 'Delhi')
print(1 in [2,3,4,5,6])


